def is_same_tree(p, q):
    if not p and not q:
        return True
    elif not p or not q:
        return False
    elif p.val != q.val:
        return False
    else:
        return is_same_tree(p.left, q.left) and is_same_tree(p.right, q.right)
from typing import List

class Solution:
    def pathInZigZagTree(self, label: int) -> List[int]:
        path = []
        while label >= 1:
            path.append(label)
            level = int(label.bit_length()) - 1
            label = (3 * (1 << level) - 1 - label) // 2
        return path[::-1]

def pathInZigZagTree(label: int) -> List[int]:
    return Solution().pathInZigZagTree(label)