from collections import deque
from typing import Optional

class Solution:
    def kthLargestLevelSum(self, root: Optional[TreeNode], k: int) -> int:
        if not root:
            return -1
        
        # Queue for level order traversal
        queue = deque([root])
        level_sums = []
        
        # Perform level order traversal
        while queue:
            level_sum = 0
            level_size = len(queue)
            for _ in range(level_size):
                node = queue.popleft()
                level_sum += node.val
                if node.left:
                    queue.append(node.left)
                if node.right:
                    queue.append(node.right)
            level_sums.append(level_sum)
        
        # Sort the level sums in descending order
        level_sums.sort(reverse=True)
        
        # Return the kth largest level sum if it exists
        return level_sums[k-1] if k <= len(level_sums) else -1

def kthLargestLevelSum(root: Optional[TreeNode], k: int) -> int:
    return Solution().kthLargestLevelSum(root, k)