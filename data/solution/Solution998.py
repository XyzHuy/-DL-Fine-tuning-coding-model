def is_same_tree(p, q):
    if not p and not q:
        return True
    elif not p or not q:
        return False
    elif p.val != q.val:
        return False
    else:
        return is_same_tree(p.left, q.left) and is_same_tree(p.right, q.right)
# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def insertIntoMaxTree(self, root: Optional[TreeNode], val: int) -> Optional[TreeNode]:
        # If the root is None or the value to insert is greater than the root's value,
        # create a new node with val and make the current tree the left subtree of this new node.
        if not root or val > root.val:
            return TreeNode(val, left=root)
        
        # Otherwise, recursively insert the value into the right subtree.
        root.right = self.insertIntoMaxTree(root.right, val)
        
        return root

def insertIntoMaxTree(root: Optional[TreeNode], val: int) -> Optional[TreeNode]:
    return Solution().insertIntoMaxTree(root, val)