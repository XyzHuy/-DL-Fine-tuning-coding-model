class TreeNode:
    def __init__(self, val=0, left=None, right=None):
        self.val = val
        self.left = left
        self.right = right

def tree_node(values: list):
    if not values:
        return None
    root = TreeNode(values[0])
    i = 1
    queue = deque()
    queue.append(root)
    while queue:
        node = queue.popleft()
        if i < len(values) and values[i] is not None:
            node.left = TreeNode(values[i])
            queue.append(node.left)
        i += 1
        if i < len(values) and values[i] is not None:
            node.right = TreeNode(values[i])
            queue.append(node.right)
        i += 1
    return root

def is_same_tree(p, q):
    if not p and not q:
        return True
    elif not p or not q:
        return False
    elif p.val != q.val:
        return False
    else:
        return is_same_tree(p.left, q.left) and is_same_tree(p.right, q.right)
# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def sumEvenGrandparent(self, root: Optional[TreeNode]) -> int:
        def dfs(node, parent, grandparent):
            if not node:
                return 0
            
            # Calculate the sum for the current node if its grandparent is even-valued
            current_sum = node.val if grandparent and grandparent.val % 2 == 0 else 0
            
            # Recursively calculate the sum for left and right children
            left_sum = dfs(node.left, node, parent)
            right_sum = dfs(node.right, node, parent)
            
            return current_sum + left_sum + right_sum
        
        # Start DFS with initial parent and grandparent as None
        return dfs(root, None, None)

def sumEvenGrandparent(root: Optional[TreeNode]) -> int:
    return Solution().sumEvenGrandparent(root)