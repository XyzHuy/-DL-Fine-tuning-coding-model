def is_same_tree(p, q):
    if not p and not q:
        return True
    elif not p or not q:
        return False
    elif p.val != q.val:
        return False
    else:
        return is_same_tree(p.left, q.left) and is_same_tree(p.right, q.right)
# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def sortedListToBST(self, head: Optional[ListNode]) -> Optional[TreeNode]:
        if not head:
            return None
        
        # Helper function to convert a linked list to array
        def convertListToArray(head):
            result = []
            while head:
                result.append(head.val)
                head = head.next
            return result
        
        # Helper function to build a BST from the sorted array
        def buildBST(nums, left, right):
            if left > right:
                return None
            mid = (left + right) // 2
            node = TreeNode(nums[mid])
            node.left = buildBST(nums, left, mid - 1)
            node.right = buildBST(nums, mid + 1, right)
            return node
        
        # Convert the linked list to an array
        nums = convertListToArray(head)
        
        # Build the BST from the sorted array
        return buildBST(nums, 0, len(nums) - 1)

def sortedListToBST(head: Optional[ListNode]) -> Optional[TreeNode]:
    return Solution().sortedListToBST(head)