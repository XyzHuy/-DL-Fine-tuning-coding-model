def is_same_tree(p, q):
    if not p and not q:
        return True
    elif not p or not q:
        return False
    elif p.val != q.val:
        return False
    else:
        return is_same_tree(p.left, q.left) and is_same_tree(p.right, q.right)
# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def closestValue(self, root: Optional[TreeNode], target: float) -> int:
        closest = root.val
        while root:
            # Update closest if the current node is closer to the target
            if abs(target - root.val) < abs(target - closest):
                closest = root.val
            # Move to the left or right child based on the target value
            if target < root.val:
                root = root.left
            else:
                root = root.right
        return closest

def closestValue(root: Optional[TreeNode], target: float) -> int:
    return Solution().closestValue(root, target)