class Solution:
    def getTargetCopy(self, original: TreeNode, cloned: TreeNode, target: TreeNode) -> TreeNode:
        # Perform a depth-first search on both trees simultaneously
        def dfs(orig: TreeNode, clone: TreeNode) -> TreeNode:
            if not orig:
                return None
            if orig == target:
                return clone
            # Search in the left subtree
            left_result = dfs(orig.left, clone.left)
            if left_result:
                return left_result
            # Search in the right subtree
            return dfs(orig.right, clone.right)
        
        return dfs(original, cloned)

def getTargetCopy(original: TreeNode, cloned: TreeNode, target: TreeNode) -> TreeNode:
    return Solution().getTargetCopy(original, cloned, target)